public class Dog {
    String name, breed;

    public String getName() {
        return name;
    }
    public String getBreed() {
        return breed;
    }

    public String setName(String DogName) {
        this.name = DogName;
        return DogName;
    }
    public String setBreed(String DogBreed) {
        this.breed = DogBreed;
        return DogBreed;
    }
}
